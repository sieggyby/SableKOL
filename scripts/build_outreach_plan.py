def vibe_fit_score(candidate, themes: list[str]) -> float:
    """Cheap theme-keyword overlap score in [0, 1].

    Tokenize bio + display_name into lowercase word-prefixes; count matches
    against the themes list (themes themselves split into words first).
    Score = unique_matches / max_possible. Bounded at 1.0.

    Phase 3 of the generalization plan replaces this with embeddings.
    """
    if not themes:
        return 0.0
    text = ((candidate.bio_snapshot or "") + " " + (candidate.display_name or "")).lower()
    words = set(re.findall(r"[a-z][a-z0-9_-]{2,}", text))
    theme_tokens: set[str] = set()
    for t in themes:
        for w in re.findall(r"[a-z][a-z0-9_-]{2,}", t.lower()):
            theme_tokens.add(w)
    if not theme_tokens:
        return 0.0
    matches = words & theme_tokens
    # Cap at 1.0 — saturate at 6+ matches.
    return min(1.0, len(matches) / 6.0)


def filter_candidates(candidates, *, include_orgs: bool, include_celebs: bool):
    """Apply org + celebrity filters and return (kept, dropped_with_reasons)."""
    kept = []
    dropped: list[tuple[str, str]] = []
    for c in candidates:
        archetypes = list(c.archetype_tags or [])
        bio = c.bio_snapshot
        h = c.handle_normalized
        if not include_orgs and is_organization(h, archetypes, bio):
            dropped.append((h, "organization"))
            continue
        if not include_celebs and is_celebrity(h, c.followers_snapshot, c.following_count):
            dropped.append((h, "celebrity"))
            continue
        kept.append(c)
    return kept, dropped


def stealth_tier(target, vibe: float, manual_pin: bool, thresholds) -> str:
    """Stealth-mode tier assignment driven by client config.

    Per /grill-me Q-stealth + audit: Tier-A is sub-reach-cap micro-influencers
    where EITHER vibe-fit OR broker count clears their respective floor.
    Manual pins always Tier-A.

    `thresholds` is a `TierThresholds` dataclass from `client_config`.
    Each tier dataclass exposes optional `max_followers`, `min_followers`,
    `min_brokers`, `min_vibe`. Missing fields are treated as "no constraint".
    """
    if manual_pin:
        return "A"
    f = target.reach_total
    brokers = len(target.social_proximity_brokers)

    def _passes(tier) -> bool:
        if tier.max_followers is not None and f > tier.max_followers:
            return False
        if tier.min_followers is not None and f < tier.min_followers:
            return False
        # min_brokers OR min_vibe — either clears the bar
        gate_brokers = tier.min_brokers is not None and brokers >= tier.min_brokers
        gate_vibe = tier.min_vibe is not None and vibe >= tier.min_vibe
        if (tier.min_brokers is not None) or (tier.min_vibe is not None):
            return gate_brokers or gate_vibe
        return True

    if _passes(thresholds.A):
        return "A"
    if _passes(thresholds.B):
        return "B"
    if _passes(thresholds.C):
        return "C"
    return "unranked"


def public_tier(target, manual_pin: bool, thresholds) -> str:
    """Public-mode tier — classic reach-descending.

    Tier-A: followers >= thresholds.A.min_followers
    Tier-B: followers >= thresholds.B.min_followers
    Tier-C: followers >= thresholds.C.min_followers
    Below all → unranked.
    Manual pins always Tier-A.
    """
    if manual_pin:
        return "A"
    f = target.reach_total
    if thresholds.A.min_followers is not None and f >= thresholds.A.min_followers:
        return "A"
    if thresholds.B.min_followers is not None and f >= thresholds.B.min_followers:
        return "B"
    if thresholds.C.min_followers is not None and f >= thresholds.C.min_followers:
        return "C"
    return "unranked"


def _generate_run(
    conn,
    *,
    config: ClientConfig,
    mode: str,
    top_k: int,
    include_orgs: bool,
    include_celebs: bool,
    kings,
    co_follow_matrix,
    clusters,
):
    """One pass: filter, vibe-score, tier, build payload + serializations.

    Returns ``(payload_dict, csv_rows, kingmakers_payload)`` ready to write.
    Splitting this out so we can call it twice — once for the filtered
    output, once for the ``_full`` variant.
    """
    # 1. Load sector-filtered candidates from the bank.
    raw_candidates = load_candidates_sector_filtered(
        conn, sector_focus=config.sector_focus, top_k=top_k * 2
    )

    # 2. Apply org + celebrity filters BEFORE ranking (configurable).
    candidates, dropped = filter_candidates(
        raw_candidates,
        include_orgs=include_orgs,
        include_celebs=include_celebs,
    )
    candidates = candidates[:top_k]

    # 3. Compute vibe-fit per candidate (themes from config).
    manual_pin_set = {h.lower() for h in config.manual_pins}
    vibe_by_handle: dict[str, float] = {}
    for c in candidates:
        vibe_by_handle[c.handle_normalized] = vibe_fit_score(c, config.themes)

    # 4. Build the plan via outreach_plan.build_plan, then re-tier per mode.
    targets = build_plan(
        conn,
        candidates=candidates,
        top_k=top_k,
        co_follow_matrix=co_follow_matrix,
        clusters=clusters,
        manual_pins=manual_pin_set,
    )

    thresholds = config.tier_thresholds[mode]
    for t in targets:
        vibe = vibe_by_handle.get(t.handle, 0.0)
        pin = t.handle in manual_pin_set
        if mode == "stealth":
            t.tier = stealth_tier(t, vibe, pin, thresholds)
        else:
            t.tier = public_tier(t, pin, thresholds)
        t.suggested_theme = (
            f"vibe={vibe:.2f}" if not t.suggested_theme else t.suggested_theme
        )

    # Sort within tier: brokers desc, vibe desc, reach asc (small first).
    tier_rank = {"A": 0, "B": 1, "C": 2, "unranked": 3}
    targets.sort(key=lambda t: (
        tier_rank.get(t.tier, 99),
        -len(t.social_proximity_brokers),
        -vibe_by_handle.get(t.handle, 0.0),
        t.reach_total,
    ))

    # Annotate each target with is_org / is_celeb so SableWeb can re-filter
    # the _full variant per query params (Codex P1-2 — partial flag handling).
    # The original raw_candidates list still has all archetypes / following_count.
    by_handle = {c.handle_normalized: c for c in raw_candidates}
    for t in targets:
        c = by_handle.get(t.handle)
        if c is None:
            t_is_org = False
            t_is_celeb = False
        else:
            archetypes = list(c.archetype_tags or [])
            t_is_org = is_organization(t.handle, archetypes, c.bio_snapshot)
            t_is_celeb = is_celebrity(t.handle, c.followers_snapshot, c.following_count)
        # Stash on the dataclass via __dict__ (slots=False on OutreachTarget
        # so this is fine). asdict() will pick them up in to_json_payload.
        t.__dict__["is_org"] = t_is_org  # type: ignore[index]
        t.__dict__["is_celeb"] = t_is_celeb  # type: ignore[index]

    payload = to_json_payload(targets)
    # to_json_payload uses asdict() which doesn't include __dict__ extras —
    # walk the payload and re-inject the booleans.
    for tier_key in ("A", "B", "C", "unranked"):
        rows = payload["targets"].get(tier_key, [])
        for row in rows:
            t = next((tt for tt in targets if tt.handle == row["handle"]), None)
            if t is not None:
                row["is_org"] = bool(t.__dict__.get("is_org", False))
                row["is_celeb"] = bool(t.__dict__.get("is_celeb", False))

    by_reason: dict[str, int] = {}
    for _h, r in dropped:
        by_reason[r] = by_reason.get(r, 0) + 1
    payload["meta"] = {
        "schema_version": 1,
        "client_id": config.client_id,
        "client_display_name": config.display_name,
        "mode": mode,
        "include_orgs": include_orgs,
        "include_celebs": include_celebs,
        "filtered_count": len(dropped),
        "filtered_by_reason": by_reason,
        "themes": config.themes,
        "network_axes": {
            "x": {"label": config.network_axes.x.label},
            "y": {"label": config.network_axes.y.label},
        },
    }

    kingmakers_payload = [
        {"handle": k.handle, "follower_count_in_pool": k.follower_count_in_pool}
        for k in kings
    ]
    csv_rows = to_csv_rows(targets)
    # Mirror is_org / is_celeb onto CSV rows so downstream tools (Hubspot
    # etc) can filter on them.
    for row in csv_rows:
        t = next((tt for tt in targets if tt.handle == row["handle"]), None)
        if t is not None:
            row["is_org"] = "1" if t.__dict__.get("is_org") else "0"
            row["is_celeb"] = "1" if t.__dict__.get("is_celeb") else "0"
    return payload, csv_rows, kingmakers_payload


def _write_variant(
    out_dir: Path,
    *,
    client_id: str,
    mode: str,
    date_slug: str,
    is_full: bool,
    payload: dict,
    csv_rows: list[dict],
    kingmakers_payload,
) -> dict[str, Path]:
    """Write the four artifacts (.md, .json, .csv plus leads.{json,csv}) for
    one variant. Returns ``{file_kind: path}`` mapping for symlink updates.

    Filename pattern (Codex P1-1 + audit #6):
        {client}_{doc}_{date}_{mode}[_full].{ext}
    where doc ∈ {report, leads}.
    """
    out_dir.mkdir(parents=True, exist_ok=True)
    full_suffix = "_full" if is_full else ""
    base = f"{client_id}_{{doc}}_{date_slug}_{mode}{full_suffix}.{{ext}}"

    paths: dict[str, Path] = {}

    # report.json
    paths["report.json"] = out_dir / base.format(doc="report", ext="json")
    paths["report.json"].write_text(json.dumps(payload, indent=2), encoding="utf-8")

    # report.md (human-facing)
    paths["report.md"] = out_dir / base.format(doc="report", ext="md")
    paths["report.md"].write_text(
        render_markdown(payload, kingmakers_payload), encoding="utf-8"
    )

    # leads.json (flat array — every visible target as one row)
    leads = []
    for tier in ("A", "B", "C", "unranked"):
        for t in payload["targets"].get(tier, []):
            leads.append(t)
    leads_payload = {
        "_meta": payload["meta"],
        "leads": leads,
    }
    paths["leads.json"] = out_dir / base.format(doc="leads", ext="json")
    paths["leads.json"].write_text(json.dumps(leads_payload, indent=2), encoding="utf-8")

    # leads.csv
    paths["leads.csv"] = out_dir / base.format(doc="leads", ext="csv")
    if csv_rows:
        with open(paths["leads.csv"], "w", encoding="utf-8", newline="") as fh:
            writer = csv.DictWriter(fh, fieldnames=list(csv_rows[0].keys()))
            writer.writeheader()
            writer.writerows(csv_rows)
    else:
        paths["leads.csv"].write_text("", encoding="utf-8")

    return paths


def _update_symlink(target: Path, link_path: Path) -> None:
    """Replace ``link_path`` with a symlink → ``target``. Uses relative path
    so /opt/sable/outreach mounts from container side still resolve."""
    try:
        if link_path.is_symlink() or link_path.exists():
            link_path.unlink()
    except OSError:
        pass
    rel = os.path.relpath(target, link_path.parent)
    try:
        link_path.symlink_to(rel)
    except OSError as exc:
        # macOS APFS can fail for cross-volume; fall back to copy.
        print(f"  warning: symlink {link_path.name} → {rel} failed ({exc}); copying instead")
        link_path.write_bytes(Path(target).read_bytes())


