"""Produce a tiered outreach plan deliverable for a Sable client.

Driven by ``~/.sable/clients/<id>.yaml`` (or production
``/opt/sable/clients/<id>.yaml``). All client-specific knobs live in
that config — themes for vibe-fit, sector_focus, audience handles,
manual pins, denylist extras, network axes, tier thresholds.

For each run, writes BOTH a filtered (default) and an unfiltered
``_full`` variant. SableWeb's API serves the filtered file from cache
when query params don't override; loads the ``_full`` file and
re-applies the filter the operator did NOT request to disable
otherwise (audit P1-2).

Tiering modes:
  * **stealth** (default for SolStitch): inverted reach. Tier-A is
    sub-15K micro-influencers with high in-pool penetration OR high
    vibe-fit.
  * **public**: classic reach-descending tiers.

Filters (always applied; ``--include-orgs`` and ``--include-celebs`` override):
  * `is_organization` from `sable_kol.filters`
  * `is_celebrity` — Vitalik / CZ / Elon / Saylor class

Tags are NOT baked into the deliverables (audit #2). SableWeb merges
live tag state from Postgres at request time before streaming downloads.

Usage:
    .venv/bin/python scripts/build_outreach_plan.py --client solstitch
        [--mode stealth|public]
        [--output-dir PATH]
        [--include-orgs]                # write only the _full variant
        [--include-celebs]              # write only the _full variant
        [--top-k 250]
"""
