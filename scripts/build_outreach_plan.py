def load_candidates_sector_filtered(conn, sector_focus: list[str], top_k: int):
    """Pull live + classified rows whose sector_tags intersect ``sector_focus``.

    Sector focus comes from the per-client config — what's "fashion+culture+art"
    for SolStitch may be "infra+gaming" for TIG. Hardcoded SQL LIKE patterns
    are gone; we OR-join over the client's configured set.
    """
    if not sector_focus:
        # No sector filter — let everything through; downstream tiering filters.
        sector_clause = "1=1"
        params: dict = {"k": top_k}
    else:
        # Build OR-joined LIKE clauses with named bindings.
        likes = []
        params = {"k": top_k}
        for i, sec in enumerate(sector_focus):
            key = f"sec{i}"
            likes.append(f"sector_tags_json LIKE :{key}")
            params[key] = f"%{sec}%"
        sector_clause = "(" + " OR ".join(likes) + ")"
    sql = f"""
      SELECT * FROM kol_candidates
      WHERE is_unresolved = 0
        AND status = 'active'
        AND archetype_tags_json != '[]'
        AND {sector_clause}
      ORDER BY (kol_strength_score IS NULL), kol_strength_score DESC,
               followers_snapshot DESC NULLS LAST
      LIMIT :k
    """
    from sable_kol.db import _row_to_candidate
    rows = conn.execute(sql, params).fetchall()
    return [_row_to_candidate(r) for r in rows]


